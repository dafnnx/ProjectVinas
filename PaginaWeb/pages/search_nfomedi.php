
      Permite la creación de CONSULTAS MÉDICAS, así como ver el EXPEDIENTE o lista de CONSULTAS del RESIDENTE elegido.<br><br>
Presiona el icono de BUSCAR para que se despliegue la lista de RESIDENTES y así puedas elegir el RESIDENTE al cual realizaras la CONSULTA o ver sus EXPEDIENTES.
Al seleccionar el RESIDENTE se desplegarán 2 opciones los cuales son:<br><br>

<strong>Nota medica:</strong><br>
Permite crear una nueva nota médica, con los datos generales de una CONSULTA, puedes elegir la fecha que desees para el registro la misma.
Los campos incluidos en este apartado, son los campos mas comunes usados en una CONSULTA MEDICA, propiamente usados por personal calificado.
* En caso de que el USUARIO con el que ingresaste al sistema no sea del AREA médico, se requerirá especificar algún medico en la lista desplegable de abajo.
Al finalizar la captura de los datos que requieras, da click en GUARDAR, se guardara la consulta en el EXPEDIENTE del RESIDENTE.<br><br>

<strong>Expediente:</strong><br>
Te proporciona la lista de EXPEDIENTES capturados para el RESIDENTE seleccionado, te muestra los siguientes detalles:<br>
el numero consecutivo autogenerado del EXPEDIENTE.<br>
Fecha que elegiste para la captura.<br>
Motivo o indicio con el que capturaste la CONSULTA.<br>
Medico que realizo la CONSULTA.<br>
Botón para ver la CONSULTA en pantalla.<br>
Botón para Generar un PDF de la CONSULTA.<br><br>

** Además de lo anterior, el sistema te permite crear SEGUIMIENTOS CONSECUTIVOS de una consulta, si te es necesario.<br><br>
Puedes crear SUBCONSULTAS, es decir, crear una nueva consulta RELACIONADA con otra, a la cual le llamaremos SEGUIMIENTO DE CONSULTA.<br><br>
Esto te permitirá poder en determinado caso, crear un hilo de CONSULTAS para llevar una secuencia y eficientizar la búsqueda en el EXPEDIENTE.<br><br>

Para crear un SEGUIMIENTO, basta con visualizar la consulta deseada con el botón de ver CONSULTA en pantalla, posteriormente, en la parte inferior veras un botón llamado Crear SEGUIMIENTO.<br><br>

Al darle click, notaras que los campos se limpian y te indicaran que es un SEGUIMIENTO de la CONSULTA seleccionada. <br><br>

Llena los campos con la información correspondiente y luego da click en GUARDAR.<br><br>
En el apartado SEGUIMIENTOS, veras que se agrega el SEGUIMIENTO que acabas de crear, para visualizarlo solo basta con presionar el botón VER.<br><br>