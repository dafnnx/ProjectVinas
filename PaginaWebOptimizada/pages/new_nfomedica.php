      A continuación, se describe la función de cada campo, algunos de estos campos son referentes o informativos así que es totalmente a criterio si son llenados o no:<br><br>

      <strong>Código:</strong><br>
      Este campo es autogenerado, no es necesario editarlo o personalizarlo, pero lo puedes personalizar si así lo deseas, se utiliza como método de búsqueda y manipulación de los MEDICAMENTOS.<br><br>

      <strong>REF:</strong><br>
      Se utiliza para ligar o concatenar el MEDICAMENTO con algún otro sistema externo que puedan utilizar, ejemplo sistema de FACTURACIÓN ELECTRÓNICA externo.<br><br>

      <strong>Código de barras:</strong><br>
      Numero comúnmente utilizado para escanear buscar y localizar un MEDICAMENTO.<br><br>

      <strong>Principios activos:</strong><br>
      Los MEDICAMENTOS pueden tener uno o mas PRINCIPIOS ACTIVOS, el sistema tiene la capacidad de agregar uno o mas para cada MEDICAMENTO. Esto te servirá para ubicar el MEDICAMENTO que desees utilizar en determinado TRATAMIENTO, sin importar MARCA o simplemente diferenciar un MEDICAMENTO genérico o patente dependiendo del caso.<br><br>     

